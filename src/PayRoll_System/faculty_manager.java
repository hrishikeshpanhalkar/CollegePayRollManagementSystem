    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name=txt2.getText();
        String department=jC2.getSelectedItem().toString();
        String uname=txt6.getText();
        String pass=String.valueOf(jP1.getPassword());
        String SType=jC1.getSelectedItem().toString();
        String destination=jC.getSelectedItem().toString();
        String d1,d2;
        String sal="0";
        int leaves=0;
        if(SType.equals("C.H.B.") || SType.equals("Regular")){
            d1="null";
            d2="null";
        }else{
            DateFormat ab=new SimpleDateFormat("yyyy-MM-dd");
            d1=ab.format(jDateChooser1.getDate());
            d2=ab.format(jDateChooser2.getDate());
            sal=txtsal.getText();
            leaves=8;
        }
        if(txt2.getText().isEmpty() || txt5.getText().isEmpty() || txt6.getText().isEmpty() || pass.equals("") || sal.equals("") ){
            JOptionPane.showMessageDialog(this,"Fill all details");
        }else{
            int sal1=Integer.parseInt(sal);
            String qualification=txt5.getText();
            try{
                Class.forName("com.mysql.jdbc.Driver");
                Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/college","root","root");
                PreparedStatement ps=con.prepareStatement("insert into facultydetails(photo,faculty_name,Qualification,Destination,usrname,password,Department,Salary_Type,Date1,Date2,Sal,nOfleaves)values(?,?,?,?,?,?,?,?,?,?,?,?)");
                InputStream is=new FileInputStream(new File(s));
                ps.setBlob(1, is);
                ps.setString(2, name);
                ps.setString(3, qualification);
                ps.setString(4,destination);
                ps.setString(5, uname);
                ps.setString(6, pass);
                ps.setString(7, department);
                System.out.println(SType);
                ps.setString(8, SType);
                ps.setString(9, d1);
                ps.setString(10, d2);
                ps.setInt(11, sal1);
                ps.setInt(12, leaves);
                if(ps.executeUpdate()==1){
                    JOptionPane.showMessageDialog(this,"Faculty details Added Successfully!");
                    txt2.setText("");txt5.setText("");
                    txt6.setText("");jP1.setText("");
                    jDateChooser2.setVisible(false);txtsal.setVisible(false);jLabel10.setVisible(false);
                    jLabel7.setVisible(false);jLabel14.setVisible(false);
                }else{
                    JOptionPane.showMessageDialog(this,"Unble to Add");
                }
            }catch(SQLIntegrityConstraintViolationException e){
                JOptionPane.showMessageDialog(this,"Duplicate Entry!");
            }catch(Exception e){
                JOptionPane.showMessageDialog(this,e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

