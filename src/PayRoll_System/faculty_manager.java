    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String fname=txt7.getText();
        String uname=txt9.getText();
        String pass=String.valueOf(txt10.getPassword());
        String sal=csal.getSelectedItem().toString();
        String destination=jComboBox1.getSelectedItem().toString();
        if (txt7.getText().isEmpty() || txt9.getText().isEmpty() || pass.equals("") || txt11.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Please fill all details or choose table row!");
        }else{
            int id=Integer.parseInt(txt12.getText());
            String qualification=txt11.getText();
            try{
                Class.forName("com.mysql.jdbc.Driver");
                Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/college","root","root");
                PreparedStatement ps=con.prepareStatement("update facultydetails Set faculty_name=?,Qualification=?,Destination=?,usrname=?,password=?,Salary_Type=? where faculty_id='"+ id+"'");
                ps.setString(1, fname);
                ps.setString(2, qualification);
                ps.setString(3,destination);
                ps.setString(4, uname);
                ps.setString(5, pass);
                ps.setString(6, sal);
                if(ps.executeUpdate()==1){
                    JOptionPane.showMessageDialog(this,"Update successfully!");
                    jtableab1();
                }else{
                    JOptionPane.showMessageDialog(this,"Unble to update!");
                }
                ps.close();

            }catch(Exception e){
                System.out.println("Error:"+e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

